XA:
;OVERWRITING DB BALANCE
GRAB 300
COPY F X
DROP

LINK 800
GRAB 200

COPY X M
MARK SEEK_ACCOUNT
TEST F = X ;USERID
SEEK 2
FJMP SEEK_ACCOUNT

SEEK -2
;WHEN WE GET A MATCH
;WE WILL BE ON THE
;FIRST CURSOR VALUE
;OF THE ACCOUNT
COPY F M ;DOLLARS
COPY F M ;CENTS
SEEK -2
COPY 0 F ;DOLLARS
COPY 0 F ;CENTS

XB:
;APPENDING DATA
LINK 800
GRAB 201
SEEK 9999

COPY M F ;DATE
COPY M F ;USER ID
COPY M F ;DOLLARS
COPY M F ;CENTS

XC:
;DATE RETRIEVER
LINK 800
LINK 801
COPY #DATE M
HALT
